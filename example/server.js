//
// @module omega.js
// ---------------------------------------------------------------------------------------------------------------------

var path = require('path');
var app = require('omega-node').app;

var news = require('./lib/news');

// ---------------------------------------------------------------------------------------------------------------------

app.router.add({
    path: '/',
    get: news.index
});

app.static.add(
    {
        url: '/static',
        path: path.join(__dirname, 'static')
    }
);

// Start the omega app.
app.listen();

